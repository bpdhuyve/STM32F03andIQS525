	//IAR
	#define PLACE_VAR_IN_SECTION(var,section)        	var @ section
	#define	PLACE_VAR_IN_NOINIT_SECTION(var,section)  __no_init var @ section
	#define NOP                                       asm("nop");
	#define RESET_HANDLER                             __iar_program_start
#elif defined(__ARMCC_VERSION)
	//KEIL
	#define PLACE_VAR_IN_SECTION(var,sec)             var __attribute__ ((section (sec))) __attribute__ ((aligned(4)))
	#define	PLACE_VAR_IN_NOINIT_SECTION(var,sec)      var __attribute__ ((section (sec), zero_init)) __attribute__ ((aligned(4)))
	#define NOP                                       __nop();
	#define RESET_HANDLER                             __main
#else
	#warning "compiler unknown..."
	#define PLACE_VAR_IN_SECTION(var,sec)             var
	#define	PLACE_VAR_IN_NOINIT_SECTION(var,sec)      var
	#define NOP   
	#define RESET_HANDLER                             main
