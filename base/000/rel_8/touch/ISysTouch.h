/// @brief	inits the module and the touch peripheral, after calling this function SysTouch_InitPad can be used
void SysTouch_Init(void);

/// @brief  configures a pin as a touch pad, after calling this function SysTouch_GetPadValue can be used
/// @param  "pad" pad from sys enum where the touch pad is located
/// @return Return TRUE if configuration was a success
BOOL SysTouch_InitPad(TOUCH_PAD pad);

/// @brief  gets the raw analog touch value (with ambient compensation) of the touch pad
/// @param  "pad" pad from sys enum where the touch pad is located
/// @return Returns the analog touch value (with ambient compensation),
/// 		the range of the value is 0-65535
/// 		the value should be on the low side when no touch is present, and on the high side when the pad is being touched
U16 SysTouch_GetPadValue(TOUCH_PAD pad);
