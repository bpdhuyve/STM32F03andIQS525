U16 StdChecksumGenerateFletcher16Part( U8 *data, U32 count )
{
   U16 sum1 = 0;
   U16 sum2 = 0;
   U32 index;

   for( index = 0; index < count; ++index )
   {
      sum1 = (sum1 + data[index]) % 255;
      sum2 = (sum2 + sum1) % 255;
   }

   return (sum2 << 8) | sum1;
}
