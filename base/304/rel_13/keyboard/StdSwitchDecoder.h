//================================================================================================//
// V E R I F Y    C O N F I G U R A T I O N
//------------------------------------------------------------------------------------------------//
#include "AppConfig.h"
//------------------------------------------------------------------------------------------------//
//================================================================================================//



//================================================================================================//
// I N C L U D E S
//------------------------------------------------------------------------------------------------//
//================================================================================================//



//================================================================================================//
// E X P O R T E D   S Y M B O L   D E F I N I T I O N S   A N D   M A C R O S
//------------------------------------------------------------------------------------------------//
// @brief   Definition of an invalid switch handle
#define INVALID_SWITCH_HNDL       255
//================================================================================================//



//================================================================================================//
// E X P O R T E D   T Y P E D E F S
//------------------------------------------------------------------------------------------------//
// @brief   Switch handle type definition
typedef U8              SWITCH_HNDL;
//================================================================================================//



//================================================================================================//
// E X P O R T E D   V A R I A B L E S
//------------------------------------------------------------------------------------------------//
//================================================================================================//



//================================================================================================//
// E X P O R T E D   F U N C T I O N   P R O T O T Y P E S
//------------------------------------------------------------------------------------------------//
// @brief   Initialises the module.
void StdSwitchDecoderInit(void);

// @brief   Function to register a switch to the module
// @return  returns a handle
SWITCH_HNDL StdSwitchDecoderRegister(void);

// @brief   Function that based on the previous calls determines if a switch is open or closed taking into account
//          anti-bounce
// @param   switch_hndl      : handle of the switch that is investigated
// @param   switch_closed    : state of the switch
//                                           '0' means switch open
//                                           '1' means switch closed
// @return  returns if the switch is truely considered closed
BOOL StdSwitchDecode(SWITCH_HNDL switch_hndl, BOOL switch_closed);
//================================================================================================//



