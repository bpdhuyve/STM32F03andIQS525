void DrvSpiMasterChannel_Init(void)
{
    LOG_DEV("DrvSpiMasterChannel_Init");
    drvspimasterchannel_msg_complete_hook = NULL;
}
//------------------------------------------------------------------------------------------------//
BOOL DrvSpiMasterChannel_RegisterMsgComplete(DRVSPI_MSG_COMPLETE msg_complete_hook)
{
    LOG_DEV("DrvSpiMasterChannel_RegisterMsgComplete");
    drvspimasterchannel_msg_complete_hook = msg_complete_hook;
    return TRUE;
}
//------------------------------------------------------------------------------------------------//
BOOL DrvSpiMasterChannel_Exchange(SPI_CHANNEL_HNDL channel_hndl, U8* write_data_ptr, U8* read_data_ptr, U16 count)
{
    LOG_DEV("DrvSpiMasterChannel_Exchange");
    
    if((channel_hndl != NULL) && (channel_hndl->hook_list_ptr != NULL) && (channel_hndl->hook_list_ptr->exchange_hook != NULL))
    {
        return channel_hndl->hook_list_ptr->exchange_hook(channel_hndl->channel_id, write_data_ptr, read_data_ptr, count);
    }
    LOG_WRN("SPI exchange function is NULL");
    return FALSE;
}
//------------------------------------------------------------------------------------------------//
BOOL DrvSpiMasterChannel_Config(SPI_CHANNEL_HNDL channel_hndl, SPI_CONFIG_STRUCT* config_struct_ptr)
{
    LOG_DEV("DrvSpiMasterChannel_Config");
    
    if((channel_hndl != NULL) && (channel_hndl->hook_list_ptr != NULL) && (channel_hndl->hook_list_ptr->config_hook != NULL))
    {
        return channel_hndl->hook_list_ptr->config_hook(channel_hndl->channel_id, config_struct_ptr);
    }
    LOG_WRN("SPI config function is NULL");
    return FALSE;
}
