//------------------------------------------------------------------------------------------------//
BOOL DrvIconDisplay_WriteData(ICON_DISPLAY_HNDL display_hndl, U16 address, U8* data_ptr, U16 size_in_bits)
{
    if((display_hndl != NULL) && (display_hndl->hook_list_ptr != NULL) && (display_hndl->hook_list_ptr->write_hook != NULL))
    {
        return display_hndl->hook_list_ptr->write_hook(display_hndl->display_id, address, data_ptr, size_in_bits);
    }
    return FALSE;
}
