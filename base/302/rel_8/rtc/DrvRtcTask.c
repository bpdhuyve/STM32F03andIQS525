static BOOL DrvRtcTask_SetTime(RTC_ID rtc_id, RTC_TIME* rtc_time_ptr);
static BOOL DrvRtcTask_GetTime(RTC_ID rtc_id, RTC_TIME* rtc_time_ptr);
static void DrvRtcTask_Timeout(VPTR data_ptr);
//================================================================================================//



//================================================================================================//
// L O C A L   V A R I A B L E S
//------------------------------------------------------------------------------------------------//
static const RTC_HOOK_LIST                  rtc_hook_list = {DrvRtcTask_SetTime, DrvRtcTask_GetTime};
static RTC_STRUCT                           rtc_struct;
static EPOCH_TIME                           rtc_epoch_time;
//================================================================================================//



//================================================================================================//
// E X P O R T E D   V A R I A B L E S
//------------------------------------------------------------------------------------------------//
//================================================================================================//



//================================================================================================//
// L O C A L   F U N C T I O N S
//------------------------------------------------------------------------------------------------//
static BOOL DrvRtcTask_SetTime(RTC_ID rtc_id, RTC_TIME* rtc_time_ptr)
{
    if(rtc_id == 0)
    {
        return DrvRtc_ConvertRtcTimetoEpochTime(*rtc_time_ptr, &rtc_epoch_time);
    }
    return FALSE;
}
//------------------------------------------------------------------------------------------------//
static BOOL DrvRtcTask_GetTime(RTC_ID rtc_id, RTC_TIME* rtc_time_ptr)
{
    if(rtc_id == 0)
    {
        return DrvRtc_ConvertEpochTimetoRtcTime(rtc_epoch_time, rtc_time_ptr);
    }
    return FALSE;
}
//------------------------------------------------------------------------------------------------//
static void DrvRtcTask_Timeout(VPTR data_ptr)
{
    rtc_epoch_time++;
}
//================================================================================================//



//================================================================================================//
// E X P O R T E D   F U N C T I O N S
//------------------------------------------------------------------------------------------------//
void DrvRtcTask_Init(void)
{
    rtc_struct.hook_list_ptr    = (RTC_HOOK_LIST*)&rtc_hook_list;
    rtc_struct.rtc_id           = 0;
    rtc_epoch_time              = 0;
    
    CoreTask_Start(CoreTask_RegisterTask(1000000, DrvRtcTask_Timeout, NULL, 100, "DRV RTC task"));
}
//------------------------------------------------------------------------------------------------//
RTC_HNDL DrvRtcTask_Register(void)
{
    return (RTC_HNDL)&rtc_struct;
}
