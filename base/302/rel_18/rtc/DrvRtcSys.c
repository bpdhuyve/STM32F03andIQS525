static BOOL DrvRtcSys_SetTime(RTC_ID rtc_id, RTC_TIME* rtc_time_ptr);
static BOOL DrvRtcSys_GetTime(RTC_ID rtc_id, RTC_TIME* rtc_time_ptr);
//================================================================================================//



//================================================================================================//
// L O C A L   V A R I A B L E S
//------------------------------------------------------------------------------------------------//
static const RTC_HOOK_LIST                  rtc_hook_list = {DrvRtcSys_SetTime, DrvRtcSys_GetTime};
static RTC_STRUCT                           rtc_struct;
//================================================================================================//



//================================================================================================//
// E X P O R T E D   V A R I A B L E S
//------------------------------------------------------------------------------------------------//
//================================================================================================//



//================================================================================================//
// L O C A L   F U N C T I O N S
//------------------------------------------------------------------------------------------------//
static BOOL DrvRtcSys_SetTime(RTC_ID rtc_id, RTC_TIME* rtc_time_ptr)
{
    if(rtc_id == 0)
    {
        return SysRtc_SetTime(rtc_time_ptr);
    }
    return FALSE;
}
//------------------------------------------------------------------------------------------------//
static BOOL DrvRtcSys_GetTime(RTC_ID rtc_id, RTC_TIME* rtc_time_ptr)
{
    if(rtc_id == 0)
    {
        return SysRtc_GetTime(rtc_time_ptr);
    }
    return FALSE;
}
//================================================================================================//



//================================================================================================//
// E X P O R T E D   F U N C T I O N S
//------------------------------------------------------------------------------------------------//
void DrvRtcSys_Init(void)
{
    SysRtc_Init();

    rtc_struct.hook_list_ptr    = (RTC_HOOK_LIST*)&rtc_hook_list;
    rtc_struct.rtc_id           = 0;
}
//------------------------------------------------------------------------------------------------//
RTC_HNDL DrvRtcSys_Register(void)
{
    return (RTC_HNDL)&rtc_struct;
}
