void DrvPwm_Init(void)
{
    LOG_DEV("DrvPwm_Init");
}
//------------------------------------------------------------------------------------------------//
BOOL DrvPwm_SetDutyCycle(PWM_CHANNEL_HNDL channel_hndl, U16 duty_cycle)
{
    LOG_DEV("DrvPwm_SetDutyCycle");

    if((channel_hndl != NULL) && (channel_hndl->hook_list_ptr != NULL) && (channel_hndl->hook_list_ptr->set_dutycycle_hook != NULL))
    {
        return channel_hndl->hook_list_ptr->set_dutycycle_hook(channel_hndl->channel_id, duty_cycle);
    }
    LOG_WRN("PWM set dutycycle function is NULL");
    return FALSE;
}
//------------------------------------------------------------------------------------------------//
BOOL DrvPwm_SetState(PWM_CHANNEL_HNDL channel_hndl, BOOL enable)
{
    LOG_DEV("DrvPwm_SetState");

    if((channel_hndl != NULL) && (channel_hndl->hook_list_ptr != NULL) && (channel_hndl->hook_list_ptr->set_state_hook != NULL))
    {
        return channel_hndl->hook_list_ptr->set_state_hook(channel_hndl->channel_id, enable);
    }
    LOG_WRN("PWM enable function is NULL");
    return FALSE;
}
