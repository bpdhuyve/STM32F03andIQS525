void SysPin_Init(void)
{
    RCC_AHBPeriphClockCmd(RCC_AHBPeriph_GPIOA |
                          RCC_AHBPeriph_GPIOB |
                          RCC_AHBPeriph_GPIOC |
                          RCC_AHBPeriph_GPIOD |
                          RCC_AHBPeriph_GPIOF, ENABLE);
}
//------------------------------------------------------------------------------------------------//
void SysPin_InitPinMask(GPIO_PORT io_port, U32 io_pin_mask, SYS_PIN_FUNC pin_func)
{
    GPIO_TypeDef*   io_port_ptr = (GPIO_TypeDef*)(GPIOA_BASE + (io_port * 0x400));
    U8              i;
    U32             mask_32 = 0;
    U32             mask_64[2] = {0, 0};
    
    for(i=0; i<16; i++)
    {
        if(io_pin_mask & (0x0001 << i))
        {
            mask_32 |= 0x0001 << (2*i);
            mask_64[(i >> 3)] |= 0x0001 << (4 * (i & 0x07));
        }
    }
    
    // SET MODE
    io_port_ptr->MODER &= ~(mask_32 * 3);
    io_port_ptr->MODER |= mask_32 * ((U32)pin_func & 0x03);
    
    pin_func >>= 4;
    
    // SET OTYPE
    io_port_ptr->OTYPER &= ~io_pin_mask;
    io_port_ptr->OTYPER |= io_pin_mask * ((U32)pin_func & 0x01);
    
    pin_func >>= 2;
    
    // SET PUPD
    io_port_ptr->PUPDR &= ~(mask_32 * 3);
    io_port_ptr->PUPDR |= mask_32 * ((U32)pin_func & 0x03);
    
    pin_func >>= 2;
    
    // SET OSPEED
    io_port_ptr->OSPEEDR &= ~(mask_32 * 3);
    io_port_ptr->OSPEEDR |= mask_32 * ((U32)pin_func & 0x03);
    
    pin_func >>= 4;
    
    // SET ALTERNATIVE
    io_port_ptr->AFR[0] &= ~(mask_64[0] * 0x0F);
    io_port_ptr->AFR[1] &= ~(mask_64[1] * 0x0F);
    io_port_ptr->AFR[0] |= (mask_64[0] * ((U32)pin_func & 0x0F));
    io_port_ptr->AFR[1] |= (mask_64[1] * ((U32)pin_func & 0x0F));
}
