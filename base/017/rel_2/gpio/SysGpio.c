void SysGpio_Init(void)
{
    RCC_AHBPeriphClockCmd(RCC_AHBPeriph_GPIOA |
                          RCC_AHBPeriph_GPIOB |
                          RCC_AHBPeriph_GPIOC |
                          RCC_AHBPeriph_GPIOD |
                          RCC_AHBPeriph_GPIOF, ENABLE);
}
//------------------------------------------------------------------------------------------------//
void SysGpio_SetPinMask(GPIO_PORT io_port, U32 io_pin_mask, BOOL value)
{
    GPIO_TypeDef* io_port_ptr = (GPIO_TypeDef*)(GPIOA_BASE + (io_port * 0x400));
    
    if(value)
    {
        io_port_ptr->BSRR |= io_pin_mask;    
    }
    else
    {
        io_port_ptr->BRR |= io_pin_mask;    
    }   
}
//------------------------------------------------------------------------------------------------//
U32 SysGpio_GetPinMask(GPIO_PORT io_port, U32 io_pin_mask)
{
    GPIO_TypeDef* io_port_ptr = (GPIO_TypeDef*)(GPIOA_BASE + (io_port * 0x400));

    return(io_port_ptr->IDR & (io_pin_mask));
}
